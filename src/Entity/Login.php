namespace Studoo\Api\EcoleDirecte\Entity;

class Login
{
    private string $token;

    public function __construct(string $token, array $data = [])
    {
        $this->token = $token;
    }

    public function getToken(): string
    {
        return $this->token;
    }

    public function setToken(string $token): self
    {
        $this->token = $token;

        return $this;
    }
}